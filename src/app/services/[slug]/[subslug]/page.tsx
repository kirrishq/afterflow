export default async function ServiceSubpage({
  params,
}: {
  params: Promise<{ slug: string; subslug: string }>
}) {
  const { slug, subslug } = await params
  if (!(slug in serviceBySlug)) {
    notFound()
  }

  const service = serviceBySlug[slug as ServiceKey]
  const sub = service.subservices.find((item) => item.slug === subslug)
  if (!sub) {
    notFound()
  }

  const mapKey = `${service.slug}:${subslug}`
  const content = subpageContentMap[mapKey]
  if (!content) {
    notFound()
  }

  return (
    <main className="page-wrap">
      <ServiceSubpageTemplate
        title={sub.title}
        heroLead={content.heroLead}
        attention={content.attention}
        interestTitle={content.interestTitle}
        interestText={content.interestText}
        scope={sub.tags}
        desireTitle={content.desireTitle}
        desireText={content.desireText}
        outcomes={content.outcomes}
        priceFrom={content.priceFrom}
        timelineFrom={content.timelineFrom}
        backHref={`/services/${service.slug}`}
        backLabel={`К услуге: ${service.title}`}
      />
      <ServiceCasesSection
        title="Кейсы по направлению"
        serviceKeys={[service.slug, `${service.slug}:${subslug}`]}
      />
      <ServiceCtaCard contextTitle={sub.title} />
    </main>
  )
}
